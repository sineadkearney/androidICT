				<p><a class="anchor" name="1"></a>
				<strong>Q. What is the specification of the Tablets used in the Android Project?</strong><br>
				A. Rather then going for some of the very cheap Tablets available, the Project has carefully researched and chosen those specifications 
				that will not cause frustration for classroom use, and which offer 'value for money'. Our 'base tablet' has ....Capacitive, Multi-Point 
				Touch Screen, Resolution of 1280 x 800 (150 ppi) pixels, minimum of 6000 mAh battery for extended use.
				</p>
				
				<p><a class="anchor" name="2"></a>
				<strong>Q. How do I project my Android Desktop onto the Whiteboard?</strong><br>
				A. With a direct cable connection to your projector. Some of our Androids use 'HDMI Out', so you will need an HDMI Micro Adaptor (from 
				the Tablet itself), a HDMI Cable, and an HDMI-enabled Projector.
				</p>
				
				<p><a class="anchor" name="3"></a>
				<strong>
				Q. My Projector does not have a HDMI Input socket. How can I use it?</strong><br>
				A. At the 'other end' of the HDMI cable, you will need a HDMI-to-VGA convertor.
				</p>
				
				<p><a class="anchor" name="4"></a>
				<strong>Q. Can I connect my Android to 'Apple TV' so that I can roam around the classroom?</strong><br>
				A. 'Apple TV' is a branded and iOS-specific protocol which does not provide full desktop mirroring Android. Instead one may use the 
				iMediaShare app to project images, videos etc. A more recent innovation is that of the MiraCast protocol which does provide full desktop 
				mirroring on supported devices, using such intermediary hardware as the NetGear PTV3000
				</p>
				
				<p><a class="anchor" name="5"></a>
				<strong>
				Q. Can I connect to my non-HDMI Pojector?</strong><br>
				A. You need an adaptor such as the 'Kanex ATV Pro' to link up with a standard VGA Cable
				</p>
				
				<p><a class="anchor" name="6"></a>
				<strong>Q. Can I use the Interactive Whiteboard in order to control the Tablet?</strong><br>
				A. No. The IWB uses Drivers installed on your PC. Your IWB then communicates directly with your PC, via the USB cable. Your Tablet 
				can be projected through your Projector (by, for example, switching the Source on your Projector Remote) but the Tablet does not have 
				the IWB drivers. This is why - in this Project - we retain your classroom/teacher PC (so you can continue to run your IWB and other 
				favourite software titles). Ideally, your Projector will have two inputs: one for the PC and one for the Tablet
				</p>
				
				<p><a class="anchor" name="7"></a>
				<strong>Q. Does my classroom need to have Wifi internet access?</strong><br>
				A. At the stage of installing or updating Apps on your Tablet - or for accessing websites during class, you will need Wifi (as our 
				Tablets do not have wired-ethernet ports). Once the installation is completed, the need for Wifi is much less - although some few 
				Apps do require access during usage (these are listed elsewhere in this website).
				</p>
				
				<p><a class="anchor" name="8"></a>
				<strong>Q. Can I view or control my Student Tablets from my Desktop?</strong><br>
				A. Yes. A number of PC-based programs are avaialble. These use the Wifi connection to 'talk' to the Tablets.
				</p>
				
				<p><a class="anchor" name="9"></a>
				<strong>Q. Can I install Android Apps from a 'store'?</strong><br>
				A. You firstly register an email address with a 'store' (e.g. the Google Play Store) and you can then install the apps from there. 
				In this Project, we have created a Subject Directory of Apps which brings you to the relavent link in the Google Play Store.
				</p>
				
				<p><a class="anchor" name="10"></a>
				<strong>Q. Can I install Androd Apps directly, or must I go via some 'store'?</strong><br>
				A. Andoid Apps can be installed directly ...if you have the original '.apk' file. Check that you know the source of the file as 
				unidentified .apks can carry potential risks.
				</p>
				
				<p><a class="anchor" name="11"></a>
				<strong>Q. Can I install a group of .apk files all at once?</strong><br>
				A. Yes, there are Programs for your PC that will do 'batch installations' via USB connection to your Tablet, without the need to copy 
				each of the .apk files onto the Tablet.
				</p>
				
				<p><a class="anchor" name="12"></a>
				<strong>Q. Can I install a number of Apps across multiple Tablets?</strong><br>
				A. Yes, you can install your apps on as many of your devices as you like, so long as they are connected to the Google Email Account 
				used to acquire the apps via the Google Play Store i.e. on your first visit to the Store you use your account to aquire the Apps. On 
				your next device, you log in to the Store with the same Account: the Store will then tell you which of your Apps are installed (or not) 
				on this second device etc.
				</p>
				
				<p><a class="anchor" name="13"></a>
				<strong>Q. Do I have to give my Credit Card details for the Google Play Store?</strong><br>
				A. Your Credit Card is not needed for the host of free educational apps in the Play Store ...so you need not have any concerns in handing 
				the Tablets out to the Students!
				</p>
				
				<p><a class="anchor" name="14"></a>
				<strong>Q. Can I 'lock down' the Tablets in order to control access to apps?</strong><br>
				A. Yes, solutions are available which can restrict access to particular apps, websites and to prevent access to Settings
				</p>
				
				
